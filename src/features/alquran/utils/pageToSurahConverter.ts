/**
 * Convert a page range string (e.g., "page:582-604") to surah/ayah information
 *
 * @param pageRange - Page range in format "page:582-604" or "582-604"
 * @returns Formatted string like "Hal 2-3 - Al-Baqarah 1-14"
 */
export function convertPageRangeToSurahLabel(pageRange: string): string {
  const { startPage, endPage } = parsePageRange(pageRange);

  // Get surah info for start and end pages
  const startSurah = PAGE_TO_SURAH_MAP[startPage];
  const endSurah = PAGE_TO_SURAH_MAP[endPage];

  if (!startSurah) {
    return `Hal ${startPage}-${endPage}`;
  }

  // If both pages have the same surah, combine the ayah ranges
  if (startSurah.surahName === endSurah?.surahName) {
    return `Hal ${startPage}-${endPage} - ${startSurah.surahName} ${startSurah.startAyah}-${endSurah.endAyah}`;
  }

  // Different surahs - show first surah with its full range on that page
  return `Hal ${startPage}-${endPage} - ${startSurah.surahName} ${startSurah.startAyah}-${startSurah.endAyah}`;
}

/**
 * Parse page range string to extract start and end page numbers
 */
function parsePageRange(pageRange: string): { startPage: number; endPage: number } {
  // Remove "page:" prefix if present
  const cleanRange = pageRange.replace(/^page:/i, "").trim();
  const [start, end] = cleanRange.split("-").map((p) => parseInt(p.trim(), 10));

  return { startPage: start, endPage: end || start };
}

/**
 * Get detailed surah information for a page range
 * Returns multiple surahs if the range spans across surahs
 */
export function getDetailedSurahInfo(
  pageRange: string
): {
  startPage: number;
  endPage: number;
  surahs: Array<{
    name: string;
    startAyah: number;
    endAyah: number;
  }>;
} {
  const { startPage, endPage } = parsePageRange(pageRange);
  const startSurah = PAGE_TO_SURAH_MAP[startPage];
  const endSurah = PAGE_TO_SURAH_MAP[endPage];

  if (!startSurah) {
    return {
      startPage,
      endPage,
      surahs: [],
    };
  }

  return {
    startPage,
    endPage,
    surahs: [{
      name: startSurah.surahName,
      startAyah: startSurah.startAyah,
      endAyah: endSurah?.surahName === startSurah.surahName 
        ? endSurah.endAyah 
        : startSurah.endAyah,
    }],
  };
}
